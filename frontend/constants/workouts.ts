export const getPredefinedPlans = (goal: SpecificGoal, difficulty: WorkoutDifficulty): WorkoutPlan[] => {
  try {
    console.log('ðŸ” Looking for plans with goal:', goal, 'difficulty:', difficulty);
    
    const goalPlans = PREDEFINED_PLANS[goal];
    if (!goalPlans) {
      console.log('âš ï¸ No plans found for goal:', goal);
      return [];
    }
    
    const difficultyPlans = goalPlans[difficulty];
    if (!difficultyPlans) {
      console.log('âš ï¸ No plans found for difficulty:', difficulty, 'in goal:', goal);
      return [];
    }
    
    console.log('âœ… Found', difficultyPlans.length, 'plans for', goal, difficulty);
    return difficultyPlans;
  } catch (error) {
    console.error('âŒ Error in getPredefinedPlans:', error);
    return [];
  }
};

export const getAllPredefinedPlans = (): WorkoutPlan[] => {
  console.log('ðŸ” Getting all predefined plans...');
  const allPlans: WorkoutPlan[] = [];
  
  Object.values(PREDEFINED_PLANS).forEach(goalPlans => {
    if (goalPlans && typeof goalPlans === 'object') {
      Object.values(goalPlans).forEach(difficultyPlans => {
        if (Array.isArray(difficultyPlans)) {
          allPlans.push(...difficultyPlans);
        }
      });
    }
  });
  
  console.log('ðŸ“Š Total predefined plans found:', allPlans.length);
  if (allPlans.length > 0) {
    console.log('ðŸ“‹ First plan:', allPlans[0]?.name);
    console.log('ðŸ“‹ First plan structure:', allPlans[0]);
  }
  
  return allPlans;
}; 